<b><h1>AnglingWes.com Angler of The Year 2013 Contest Rules</h1></b></p>

<p>1. All fish entered must be caught by hook and line without aid from anyone else and using methods deemed legal by the California Department of Fish and Wildlife (CDFW). This includes flossing or beading. For 2013 spearfishing or any fishing while diving is not eligible for the competition.</p>

<p>2. All fish submitted must be legal for the waters it was caught in as defined by the CDFW.</p>

<p>3. Fishing from any vessel or bank fishing is allowed. </p>

<p>4. All entries must be submitted using photographic evidence. For all fish 24" and under, the total length of the fish against a measuring device must be in the photo. Fish longer than 24" require some object in the picture that can be used for scale (ruler, bottle, can, etc.). </p>

<p>	a. The ENTIRE fish must show with the measuring device in only 1 picture. </p>

<p>	b. Fish may NOT be gutted, have their throat cut or ripped through, or manipulated in any way prior to photographs being taken. Cutting of gills is permitted. </p>

<p>	c. Mouth closed or partially open in a natural position is ok</p>

<p>5. If you do not have a measuring device, PM an AnglingWes.com moderator and we will send you a tape measure.</p>

<p>6. All entries are subject to final approval by AnglingWes.com moderators after submission. Should an entry be rejected, an AnglingWes.com moderator will contact the submitter and explain what must happen before the submission is accepts or why it is being rejected. </p>

<p>7. AOTY runs from January 20, 2013 to December 31, 2013. Fish caught prior to these dates are not eligible for submission. All submissions must be entered before 11:59pm on December 31, 2013. No entries will be allowed after December 31, 2013.</p>

<p>8. Only fish caught in California waters are eligible for submission. California waters are defined as</p>

<p>	a. South of the Oregon boarder</p>

<p>	b. North of the Mexico boarder</p>

<p>	c. West of the Nevada boarder</p>

<p>	d. West of the Arizona boarder</p>

<p>	e. Fish caught anywhere in Lake Tahoe are eligible for submission </p>

<p>9. Entries must be posted within 1 week of being caught and photographed. No "hoarding" of entries.</p>

<p>10. Participant complaints should be directed to an AnglingWes moderator. All decisions made by the AnglingWes moderator team will be final.</p>

<p>11. All rules are subject to change at the discretion of the AnglingWes moderator team. Any rule or scoring changes made after the beginning of the competition will not affect the scoring of fish submitted prior to the rule change. For example, if the scoring of Trout is changed from 1pt/inch to 2pts/inch on March 31,2013, all trout caught and submitted prior to March 31<sup>st</sup>, 2013 will not receive the new scoring. Conversely, should the scoring change from 2pts/inch to 1pt/inch, any previously submitted trout will not receive a point reduction. </p>

<p>12. All photos submitted to the AnglingWes AOTY competition become property of AnglingWes.com and can be reproduced or used by AnglingWes.com in any way.</p>

<p>13. Bottom line, this is intended to be a fun competition and thus no entry fee. Please don’t be an asshat (look it up on UrbanDictionary.com).</p>

<p><b>Scoring </b></p>

<p>1. All fish caught will be awarded 1 point per inch, plus a bonus point of 1pt per 12 inches and a 5pt bonus for every 36 inches.</p>

<p>2. No partial points will be awarded. All lengths will be rounded to the nearest inch. If a fish is 12.49 inches, it will be rounded down to 12 inches. If the fish is 12.50 inches, it will be rounded up to 13 inches.</p>

<p>3. All points from valid submissions will be counted toward the anglers points in the AOTY competition. </p>

<p>4. Only total points will be counted toward the anglers AOTY score. </p>

<p>5. All scores will be finalized by the AnglingWes.com moderators. </p>

<p><b>Winning and Prizes</b></p>

<p>1. The top 3 AOTY scores will take 1<sup>st</sup>, 2<sup>nd</sup> and 3<sup>rd</sup> place. </p>

<p>2. The top scoring anglers will win the 2013 competition and receive anything from an “Attaboy” and photo on the front page to a 2013 Ferrari F12 Berlinetta. Prizes are TBD.</p>

<p>3. There will be no prizes on a per species basis.</p>

<p><b>Contact Info</b></p>

<p>If there are any issues with an uploaded catch or picture, you can post a note on the AW Forums in the AOTY Section or email AOTY@anglingwes.com. Please include the Catch ID and date in your email. </p>





